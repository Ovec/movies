import cz.mireksirina.movies.presentation.common.MoviesList
import cz.mireksirina.movies.presentation.common.SearchBar
import cz.mireksirina.movies.util.Constants.MediumPadding1


@OptIn(ExperimentalFoundationApi::class)
@Composable
fun HomeScreen(
    movies: LazyPagingItems<Result>,
    navigateToSearch: () -> Unit,
    navigateToDetails: (Result) -> Unit
) {

    val titles by remember {
        derivedStateOf {
            if (movies.itemCount > 10) {
                movies.itemSnapshotList.items
                    .slice(IntRange(start = 0, endInclusive = 9))
                    .joinToString(separator = " \uD83D\uDFE5 ") { it.title }
            } else {
                ""
            }
        }
    }

    Column(
        modifier = Modifier
            .fillMaxSize()
            .padding(top = MediumPadding1)
            .statusBarsPadding()
    ) {
        Image(
            painter = painterResource(id = R.drawable.ic_logo),
            contentDescription = null,
            modifier = Modifier
                .width(150.dp)
                .height(30.dp)
                .padding(horizontal = MediumPadding1)
        )

        Spacer(modifier = androidx.compose.ui.Modifier.height(MediumPadding1))

        SearchBar(
            modifier = Modifier
                .padding(horizontal = MediumPadding1)
                .fillMaxWidth(),
            text = "",
            readOnly = true,
            onValueChange = {},
            onSearch = {},
            onClick = navigateToSearch
        )

        Spacer(modifier = androidx.compose.ui.Modifier.height(MediumPadding1))

        Text(
            text = titles, modifier = Modifier
                .fillMaxWidth()
                .padding(start = MediumPadding1)
                .basicMarquee(), fontSize = 12.sp,
            color = colorResource(id = R.color.placeholder)
        )

        Spacer(modifier = androidx.compose.ui.Modifier.height(MediumPadding1))

        MoviesList(
            modifier = Modifier.padding(horizontal = MediumPadding1),
            movies = movies,
            onClick = navigateToDetails
        )
    }
}